import Field from './field'
import { SubFieldProps } from '../types/subFieldProps'

export default class RichTextField extends Field {
    constructor({
        id,
        name,
        localized,
        required,
        validations,
        disabled,
        omitted,
        widgetId,
        helpText,
        format,
    }: SubFieldProps) {
        super({
            id,
            name,
            type: 'RichText',
            localized,
            required,
            validations,
            disabled,
            omitted,
            widgetId,
            helpText,
            format,
        })
    }

    getTyping = () => `{
        json: Document
    }`

    getFragment = () =>
        `${this.contentFields.id} {
            json
        }`
}
